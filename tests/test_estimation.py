@test("recover true CL_int from synthetic IV data (within 30%)")
def t():
    csv = _make_synthetic_csv(route="iv_bolus", dose_mg=5.0,
                               true_CL_int=500.0, n_points=12)
    try:
        ds = Dataset(csv_file=csv, dose_mg=5.0, route="iv_bolus")
        rep = fit_pbpk(
            datasets=[ds], compound_name="midazolam",
            params_to_fit=["CL_int"],
        )
        cl_fit = rep.fitted_params["CL_int"]
        rel_err = abs(cl_fit - 500.0) / 500.0
        assert rel_err < 0.30, \
            f"CL_int={cl_fit:.0f} vs true 500.0 (err {rel_err:.0%})"
        assert rep.r_squared > 0.8, f"R²={rep.r_squared:.2f}"
        assert not rep.best_is_penalty, "penalty leaked through"
    finally:
        os.unlink(csv)


