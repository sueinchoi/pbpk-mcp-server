PASSED: list[str] = []
FAILED: list[tuple[str, str]] = []


def test(name: str):
    def decorator(fn):
        try:
            fn()
            PASSED.append(name)
            print(f"  ✓ {name}")
        except AssertionError as e:
            FAILED.append((name, str(e)))
            print(f"  ✗ {name}: {e}")
        except Exception as e:
            FAILED.append((name, f"{type(e).__name__}: {e}"))
            print(f"  ✗ {name}: {type(e).__name__}: {e}")
        return fn
    return decorator


def expect_raises(fn, exc_type=Exception, contains: str = ""):
    try:
        fn()
    except exc_type as e:
        if contains and contains.lower() not in str(e).lower():
            raise AssertionError(f"raised but msg lacks '{contains}': {e}")
        return
    raise AssertionError(f"expected {exc_type.__name__}, got no exception")


# ---------------------------------------------------------------------
# Synthetic data fixture: simulate midazolam IV and oral with known
# parameters, write CSVs, return paths.
# ---------------------------------------------------------------------

def _make_synthetic_csv(*, route: str, dose_mg: float, true_CL_int: float,
                       true_ka: float = 1.0, n_points: int = 12,
                       noise_sd_log: float = 0.05) -> str:
    """Generate synthetic PK data by running the model with KNOWN
    parameters, then writing a CSV with log-normal noise."""
    from core.compound import COMPOUND_LIBRARY, CompoundSpec
    from core.physiology import get_physiology, Sex
    from core.pbpk_model import (
        PBPKModel, DosingProtocol, SimulationConfig, Route,
    )
    base = COMPOUND_LIBRARY["midazolam"]
    c = CompoundSpec(
        name=base.name, mw=base.mw, logP=base.logP, pKa=base.pKa,
        fu_p=base.fu_p, compound_type=base.compound_type, R_bp=base.R_bp,
        ka=true_ka, Fa=base.Fa, Fg=base.Fg,
        CL_int=true_CL_int, CL_renal=base.CL_renal,
    )
    phys = get_physiology(73.0, Sex.MALE)
    model = PBPKModel(c, phys)
    dosing = DosingProtocol(dose_mg, Route(route))
    cfg = SimulationConfig(duration_h=24.0, n_timepoints=500)
    result = model.simulate(dosing, cfg)
    # Sample at log-spaced time points
    t_obs = np.logspace(np.log10(0.25), np.log10(20.0), n_points)
    c_obs = np.interp(t_obs, result.time, result.venous_plasma)
    # Add log-normal noise
    rng = np.random.default_rng(42)
    c_obs = c_obs * np.exp(rng.normal(0, noise_sd_log, size=c_obs.shape))
    # Write CSV
    fd, path = tempfile.mkstemp(suffix=".csv", prefix=f"midaz_{route}_")
    with os.fdopen(fd, "w") as f:
        f.write("time_h,conc_mg_l\n")
        for t, c in zip(t_obs, c_obs):
            f.write(f"{t},{c}\n")
    return path


