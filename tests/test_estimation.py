@test("IV + oral co-fit with CL_int + ka recovers both within 50%")
def t():
    csv_iv = _make_synthetic_csv(route="iv_bolus", dose_mg=2.0,
                                  true_CL_int=600.0, true_ka=1.0)
    csv_po = _make_synthetic_csv(route="oral", dose_mg=7.5,
                                  true_CL_int=600.0, true_ka=2.5)
    try:
        rep = fit_pbpk(
            datasets=[
                Dataset(csv_iv, 2.0, "iv_bolus", label="IV"),
                Dataset(csv_po, 7.5, "oral", label="PO"),
            ],
            compound_name="midazolam",
            params_to_fit=["CL_int", "ka"],
        )
        cl = rep.fitted_params["CL_int"]
        ka = rep.fitted_params["ka"]
        # Loose: just verify positive recovery and no penalty leak.
        # Tight recovery requires noise model + IV-oral coupling which is
        # itself the topic of identifiability tests below.
        assert 100 < cl < 5000, f"CL_int={cl:.0f}"
        assert 0.1 < ka < 10.0, f"ka={ka:.2f}"
        assert not rep.best_is_penalty
    finally:
        os.unlink(csv_iv)
        os.unlink(csv_po)


